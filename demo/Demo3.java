public class Demo3 {
	public static void main(String[] args) {
		
		char c;
		for (int i = 'a'; i <= 'z'; i++) {
			c = (char) i;
			System.out.print(c + " ");
		}
		System.out.println();
		for (int i = 'A'; i <= 'Z'; i++) {
			c = (char) i;
			System.out.print(c + " ");
			
		}
		
	}
}
